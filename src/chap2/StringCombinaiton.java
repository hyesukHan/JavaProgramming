package chap2;

public class StringCombinaiton {
    public static void main(String[] args) {
        //다양하게 문자열을 결합해보는 예제 프로그램
        String name = "ja"+"va";
        String str = name+ "8.0";

        System.out.println(name);
        System.out.println(str);
        System.out.println(7+" ");
        System.out.println(" "+7);
        System.out.println(""+"");
        System.out.println(7+7+"");
        System.out.println(""+7+7);
    }
}
